select * from CHITIETDONHANG where MAHANG = 'MH002'

--Câu 3> Viết hàm trả về một bảng trong đó cho biết tổng số lượng hàng bán được của mỗi mặt hàng. 
--Sử dụng hàm này để thống kê xem tổng số lượng hàng (hiện có và đã bán) của mỗi mặt hàn là bao nhiêu

CREATE FUNCTION dbo.TongLuongBan()
RETURNS TABLE 
AS
RETURN
(
	SELECT h.MAHANG, h.TENHANG, SUM(c.SOLUONG) AS TongSoLuong
	FROM MATHANG h
	JOIN CHITIETDONHANG c
	ON h.MAHANG = c.MAHANG
	GROUP BY h.MAHANG, h.TENHANG
);

--Thực thi--
SELECT h.MAHANG, h.TENHANG, h.SOLUONG AS [Số lượng hiện còn], ISNULL (t.TongSoLuong, 0) AS [Số lượng đã bán], (h.SOLUONG + ISNULL(t.TongSoLuong, 0)) AS [TỔNG SỐ LƯỢNG]
FROM MATHANG h
LEFT JOIN dbo.TongLuongBan() t
ON h.MAHANG = t.MAHANG

