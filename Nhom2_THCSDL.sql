--Câu 2> Tạo thủ tục lưu trũ có chức năng thống kê tổng số lượng hàng bán được của 
---một mặt hàng có mã  bât kỳ (mã mặt hàng cần thống kê là tham số của thủ tục)
CREATE PROC ThongKeSoLuongBanHang
	@maMatHang CHAR(10)
AS
BEGIN
	SELECT h.MAHANG, h.TENHANG, SUM(c.SOLUONG) AS [Tổng số lượng bán]
	FROM MATHANG h
	JOIN CHITIETDONHANG c
	ON h.MAHANG = c.MAHANG
	WHERE h.MAHANG = @maMatHang
	GROUP BY h.MAHANG, h.TENHANG;
END
--Thực thi--
