			(số lượng hàng bán ra không được vượt quá sô lượng hàng hiện có  và không được nhỏ hơn 1).
			Nếu dữ liệu hợp lệ thì giảm (hoặc tăng) số lượng hàng hiện có trong công ty, ngược lại thì huỷ bỏ thao tác cập nhật.*/

-->Yêu cầu Insert
CREATE TRIGGER TG_CHITIETDONHANG_INSERT
ON CHITIETDONHANG 
AFTER  INSERT
AS
BEGIN
    IF EXISTS 
	(
        SELECT 1
        FROM MATHANG AS H
        JOIN inserted AS I ON H.MAHANG = I.MAHANG
        WHERE I.SoLuong > H.SoLuong AND I.SOLUONG>=1
    )
		BEGIN
			PRINT N'Không đủ số lượng hàng hiện có trong kho để bán';
			ROLLBACK;
		END
    ELSE
		BEGIN
			UPDATE MATHANG
			SET SoLuong = H.SoLuong - I.SoLuong
			FROM MATHANG AS H
			JOIN inserted AS I ON H.MAHANG = I.MAHANG;
			UPDATE	CHITIETDONHANG
			SET GIABAN= H.GIAHANG
			FROM MATHANG AS H, inserted AS I
			WHERE H.MAHANG =CHITIETDONHANG.MAHANG AND I.MAHANG=CHITIETDONHANG.MAHANG AND I.SOHOADON=CHITIETDONHANG.SOHOADON
			PRINT N'Thêm sản phẩm thành công';
		END
END;
GO

-->Yêu cầu Update
CREATE TRIGGER TG_CHITIETDONHANG_UPDATE
ON CHITIETDONHANG 
AFTER UPDATE
AS
BEGIN
    IF EXISTS 
	(
        SELECT 1
        FROM MATHANG AS MH
        JOIN inserted AS I ON MH.MAHANG = I.MAHANG
        JOIN deleted AS D ON D.MAHANG = I.MAHANG
        WHERE MH.SoLuong + D.SoLuong - I.SoLuong < 0 OR I.SoLuong < 1
    )
		BEGIN
			PRINT N'Số lượng cập nhật không hợp lệ';
			ROLLBACK ;
		END
    ELSE
		BEGIN
			UPDATE MATHANG
			SET SoLuong = MH.SoLuong + D.SoLuong - I.SoLuong
			FROM MATHANG AS MH
			JOIN inserted AS I ON MH.MAHANG = I.MAHANG
			JOIN deleted AS D ON D.MAHANG = I.MAHANG;
			UPDATE	CHITIETDONHANG
			SET GIABAN= MH.GIAHANG
			FROM MATHANG AS MH, inserted AS I
			WHERE MH.MAHANG =CHITIETDONHANG.MAHANG 
				AND I.MAHANG=CHITIETDONHANG.MAHANG
				AND I.SOHOADON=CHITIETDONHANG.SOHOADON

		END
END;
