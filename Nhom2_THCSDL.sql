--Câu 1> Tạo thủ tục lưu trữ để thông qua thủ tục này có thể bổ sung thêm một bản ghi mới cho bảng MATHANG 
--(thủ tục phải kiểm tra tính hợp lệ của dữ liệu cần bổ sung: không trùng khoa chính là đảm bảo toàn vẹn tham chiếu)

CREATE PROC PR_MATHANG_Insert
(	@maHang CHAR(10), 
	@tenHang NVARCHAR(30), 
	@maCongTy CHAR(20), 
	@maLoaiHang CHAR(20), 
	@soLuong FLOAT, 
	@donViTinh NVARCHAR(20), 
	@giaHang MONEY
)
AS
BEGIN
	IF EXISTS
	(
		SELECT 1
		FROM MATHANG AS h
		WHERE @maHang=h.MAHANG
	)
		BEGIN
			PRINT N'Mặt hàng này đã tồn tại'
			RETURN;
		END
	IF EXISTS
	(
		SELECT 1
		FROM NHACUNGCAP
		WHERE MACONGTY=@maCongTy
		UNION
		SELECT 1
		FROM LOAIHANG
		WHERE MALOAIHANG=@maLoaiHang
	)
		BEGIN
			PRINT N'Mặt hàng này không tồn tại'
			RETURN;
		END
	ELSE
		BEGIN
			INSERT INTO MATHANG(MAHANG,TENHANG,MACONGTY,MALOAIHANG,SOLUONG,DONVITINH,GIAHANG)
			VALUES (@maHang,@tenHang,@maCongTy,@maLoaiHang,@soLuong,@donViTinh,@giaHang);
		END
END
GO
